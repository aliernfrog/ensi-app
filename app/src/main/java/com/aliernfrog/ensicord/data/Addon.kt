package com.aliernfrog.ensicord.data

data class Addon(
    val name: String,
    val description: String,
    val thumbnailUrl: String? = null,
    val setAppTheme: String? = null,
    val setEnsiUserName: String? = null,
    val setEnsiWords: Set<String>? = null,
    val addEnsiWords: Set<String>? = null,
    val setEnsiVerbs: Set<String>? = null,
    val addEnsiVerbs: Set<String>? = null,
    val setEnsiTimes: Set<String>? = null,
    val addEnsiTimes: Set<String>? = null,
    val setEnsiChars: Set<String>? = null,
    val addEnsiChars: Set<String>? = null,
    val setEnsiPlaces: Set<String>? = null,
    val addEnsiPlaces: Set<String>? = null,
    val setEnsiConcs: Set<String>? = null,
    val addEnsiConcs: Set<String>? = null,
    val setEnsiPositions: Set<String>? = null,
    val addEnsiPositions: Set<String>? = null,
    val setEnsiEmotions: Set<String>? = null,
    val addEnsiEmotions: Set<String>? = null,
    val setEnsiOthers: Set<String>? = null,
    val addEnsiOthers: Set<String>? = null,
    val setEnsiNormalTypes: Set<String>? = null,
    val addEnsiNormalTypes: Set<String>? = null,
    val setEnsiQuestionTypes: Set<String>? = null,
    val addEnsiQuestionTypes: Set<String>? = null,
    val setEnsiStartingTypes: Set<String>? = null,
    val addEnsiStartingTypes: Set<String>? = null,
    var repo: String, //This is generated by Ensicord to save where add-on comes from
    val error: Boolean = false //This is generated by Ensicord when there's an error about the add-on
) {
    fun getSet(field: String): Set<String>? {
        return when(field) {
            "setEnsiWords" -> this.setEnsiWords
            "addEnsiWords" -> this.addEnsiWords
            "setEnsiVerbs" -> this.setEnsiVerbs
            "addEnsiVerbs" -> this.addEnsiVerbs
            "setEnsiTimes" -> this.setEnsiTimes
            "addEnsiTimes" -> this.addEnsiTimes
            "setEnsiChars" -> this.setEnsiChars
            "addEnsiChars" -> this.addEnsiChars
            "setEnsiPlaces" -> this.setEnsiPlaces
            "addEnsiPlaces" -> this.addEnsiPlaces
            "setEnsiConcs" -> this.setEnsiConcs
            "addEnsiConcs" -> this.addEnsiConcs
            "setEnsiPositions" -> this.setEnsiPositions
            "addEnsiPositions" -> this.addEnsiPositions
            "setEnsiEmotions" -> this.setEnsiEmotions
            "addEnsiEmotions" -> this.addEnsiEmotions
            "setEnsiOthers" -> this.setEnsiOthers
            "addEnsiOthers" -> this.addEnsiOthers
            "setEnsiNormalTypes" -> this.setEnsiNormalTypes
            "addEnsiNormalTypes" -> this.addEnsiNormalTypes
            "setEnsiQuestionTypes" -> this.setEnsiQuestionTypes
            "addEnsiQuestionTypes" -> this.addEnsiQuestionTypes
            "setEnsiStartingTypes" -> this.setEnsiStartingTypes
            "addEnsiStartingTypes" -> this.addEnsiStartingTypes
            else -> null
        }
    }
}
